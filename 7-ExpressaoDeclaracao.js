// Declaração de variável
let x = 10;

// Declaração de função
function sayHello() {
    console.log("Hello, World!");
}

// Declaração de condição
if (x > 5) {
    console.log("x é maior que 5");
}

// Declaração de loop
for (let i = 0; i < 5; i++) {
    console.log(i);
}

// Expressão aritmética
let y = 5 + 3; // A expressão "5 + 3" resulta no valor 8

// Expressão de função
let greet = function() {
    return "Hello!";
};

// Expressão lógica
let isEven = (y % 2 === 0); // A expressão "y % 2 === 0" resulta em true ou false

// Expressão condicional (ternária)
let z = (x > 5) ? "Maior que 5" : "Menor ou igual a 5";

// Declaração de variável usando uma expressão
let result = 2 + 3; // A expressão "2 + 3" é avaliada, e o resultado (5) é armazenado em 'result'

// Declaração de função que retorna uma expressão
function add(a, b) {
    return a + b; // A expressão "a + b" é avaliada e retorna o valor resultante
}

// Chamada da função (declaração) que resulta em uma expressão
let sum = add(4, 6); // A expressão "add(4, 6)" é avaliada, resultando no valor 10
